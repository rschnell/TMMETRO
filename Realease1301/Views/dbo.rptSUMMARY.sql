CREATE VIEW dbo.rptSUMMARY
AS
SELECT YEAR([Date Raised]) AS YEAR, MONTH([Date Raised]) AS MONTH, [Date Raised] AS DATE, [Country Code], [COMPANY CODE] AS COMPANYCODE, COMPANY AS COMPANYNAME, 
               [Incident Type] AS INCIDENTTYPE, LEFT(FACILITY, 9) AS FACILITYCODE, FACILITY, CASEID AS CASES, [PRIMARY], SECONDARY, SUBSTRING(FACILITY, 11, LEN(FACILITY) - 10) 
               AS SHORTCODE, Consequences, PERUNIT, RATETOEUR, [Currency Code], CAST([Recovered Local] / RATETOEUR AS DECIMAL(18, 0)) AS Recovered, 
               CAST([Total Loss Local] / RATETOEUR AS DECIMAL(18, 0)) AS Loss, [Recovered Local], [Total Loss Local]
FROM  dbo.rptCASESMETROB
