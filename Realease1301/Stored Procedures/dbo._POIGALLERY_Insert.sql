	(
		@ID,
		@PERSONID,
		@SUBSCRIPTIONID,
		@CASEID,
		@ACTIONID,
		@PICTURE,
		@UPDATED,
		@UPDATEDBY,
		@TITLE,
		@DESCRIPTION,
		@DATETAKEN,
		@PICTYPE,
		@EXT
