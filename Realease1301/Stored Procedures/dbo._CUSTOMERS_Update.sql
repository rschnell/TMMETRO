	-- The rest of writeable parameters
	@PERSONID int,
	@USERID int,
	@REGNO nvarchar(50),
	@COMPANYCODE nchar(10),
	@COMPANYNAME nvarchar(70),
	@ADDRESSLINE1 nchar(70),
	@ADDRESSLINE2 nchar(70),
