	@RATE3PERHOUR numeric(18,3),
	@RATE4PERHOUR numeric(18,3),
	@RATE1TEXT nvarchar(50),
	@RATE2TEXT nvarchar(50),
	@RATE3TEXT nvarchar(50),
	@RATE4TEXT nvarchar(50),
	-- Primary key parameters
	@CUSTOMERID int
