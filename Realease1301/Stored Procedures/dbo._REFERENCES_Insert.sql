-- Inserts a new record into the 'REFERENCES' table.
CREATE PROCEDURE [dbo].[_REFERENCES_Insert]
	@REFERENCE nvarchar(50),
	@REFTYPE int,
	@PERSON int,
	@CASEID int
AS
